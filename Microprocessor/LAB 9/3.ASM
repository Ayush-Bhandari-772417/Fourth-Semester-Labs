.MODEL SMALL
.STACK 64
.DATA
	STRLEN DB 255
	STRSZ DB ?
	STR DB 255 DUP('$')
	MSG1 DB "Number of words = $"
.CODE
	MOV AX, @DATA
	MOV DS, AX
	
;TAKING INPUT FROM USER
	LEA DX, STRLEN
	MOV AH, 0AH
	INT 21H

;COUNTING NUMBER OF WORDS
	MOV AX, 1
	MOV CX, 0
	MOV CL, STRSZ
	LEA BX, STR
L2:	CMP BYTE PTR[BX], ' '
	JNE EX
	CMP BYTE PTR[BX+1], ' '
	JE EX
	INC AX
EX:   INC BX
	LOOP L2

	MOV BX, 10

;EXTRACTING DIGITS FROM NUMBER TO DISPLAY
L3:	MOV DX, 00
	INC CX
	DIV BX
	CMP AX, 00
	PUSH DX
	JG L3

;MOVING CURSOR TO NEXT LINE
	MOV AH, 02
	MOV DL, 0AH
	INT 21H

;DISPLAYING TEXT
	LEA DX, MSG1
	MOV AH, 09
	INT 21H

;DISPLAYING THE ACTUAL NUMBER
DIS:	POP DX
	OR DX, 30H
	MOV AH, 02
	INT 21H
	LOOP DIS
	
;ENDING PROGRAM
	MOV AH, 4CH
	INT 21H
	END