.MODEL SMALL
.STACK 64

.DATA
	NUMS DW 100, 200, 300, 400, 500, 600, 700, 800, 900, 10000
	SUMS DW ?
	MSG1 DB "Sum of numbers = $"

.CODE
	MOV AX, @DATA
	MOV DS, AX

	MOV CX, 10
	LEA BX, NUMS
	MOV AX, 00

;CALCULATING SUM

L2:	ADD AX, [BX]
	INC BX
	INC BX
	LOOP L2

	MOV SUMS, AX
	MOV CX, 00
	MOV BX, 10

;CONVERTING NUMBER TO TEXT
L3:	MOV DX, 00
	INC CX
	DIV BX
	CMP AX, 00
	PUSH DX
	JG L3

;DISPLAYING MESSAGE
	LEA DX, MSG1
	MOV AH, 09
	INT 21H

;DISPLAYING CALCULATED SUM
DIS:	POP DX
	OR DX, 30H
	MOV AH, 02H
	INT 21H
	LOOP DIS

	MOV AH, 4CH
	INT 21H
END

;OUTPUT 14500