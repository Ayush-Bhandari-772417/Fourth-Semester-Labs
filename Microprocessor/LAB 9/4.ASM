.MODEL SMALL
.STACK 64

.DATA
	STRLEN DB 255
	STRSZ DB ?
	STR DB 255 DUP ('$')
	MSG1 DB "Number of vowels = $"

.CODE
	MOV AX, @DATA
	MOV DS, AX

;TAKING INPUT FROM USER
	LEA DX, STRLEN
	MOV AH, 0AH
	INT 21H

;COUNTING NUMBER OF VOWELS
	MOV AX, 00
	MOV CX, 00
	MOV CL, STRSZ
	LEA BX, STR

L2:	CMP BYTE PTR[BX], 'a'
	JE AD
	CMP BYTE PTR[BX], 'e'
	JE AD
	CMP BYTE PTR[BX], 'i'
	JE AD
	CMP BYTE PTR[BX], 'o'
	JE AD
	CMP BYTE PTR[BX], 'u'
	JE AD
	CMP BYTE PTR[BX], 'A'
	JE AD
	CMP BYTE PTR[BX], 'E'
	JE AD
	CMP BYTE PTR[BX], 'I'
	JE AD
	CMP BYTE PTR[BX], 'O'
	JE AD
	CMP BYTE PTR[BX], 'U'
	JE AD
	JMP EX

AD:	INC AX
EX:	INC BX
	LOOP L2

	MOV BX, 10

;CONVERTING NUMBER TO TEXT
L3:	MOV DX, 00
	INC CX
	DIV BX
	CMP AX, 00
	PUSH DX
	JG L3

	MOV AH, 02
	MOV DL, 0AH
	INT 21H

;DISPLAYING MESSAGE
	LEA DX, MSG1
	MOV AH, 09
	INT 21H

;DISPLAYING COUNTED NUMBER
DIS:	POP DX
	OR DX, 30H
	MOV AH, 02H
	INT 21H
	LOOP DIS

	MOV AH, 4CH
	INT 21H
END