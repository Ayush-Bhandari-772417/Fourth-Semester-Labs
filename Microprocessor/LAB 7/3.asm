.MODEL SMALL
.STACK 32

.DATA
STRLEN DB 255
STRSZ DB 0
STR DB 255 DUP('$')
MSG1 DB "Enter the string : $"
MSG2 DB 0DH, 0AH, "Your string is : $"

.CODE
	MOV AX, @DATA
	MOV DS, AX
	LEA DX, MSG1
	MOV AH, 09H
	INT 21H
	LEA DX, STRLEN
	MOV AH, 0AH
	INT 21H
	LEA DX, MSG2
	MOV AH, 09H
	INT 21H
	LEA DX, STR
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H
END