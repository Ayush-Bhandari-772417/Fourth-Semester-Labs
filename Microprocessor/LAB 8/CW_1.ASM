.MODEL SMALL
.STACK 32
.DATA
TBL1 DB 20H, 40H, 60H, 10H, 70H

.CODE

MOV AX, @DATA
MOV DS, AX

;clearing screen
MOV AH,00H
MOV AL,03H
INT 10H

;making container
MOV SI, OFFSET TBL1

MOV AH, 06
MOV AL, 00

MOV CH, 00
MOV CL, 00
MOV DH, 03
MOV DL, 06
MOV BH, [SI]
INT 10H

L1:
INC SI
ADD CH, 04H
ADD CL, 07H
ADD DH, 04H
ADD DL, 07H
MOV BH, [SI]
INT 10H
CMP [SI], 70H
JNE L1

;moving cursor and displaying characters
MOV AH, 02
MOV BH, 00

MOV DH, 1
MOV DL, 3
INT 10H
MOV DL, 'H'
INT 21H

MOV DH, 5
MOV DL, 10
INT 10H
MOV DL, 'E'
INT 21H

MOV DH, 9
MOV DL, 17
INT 10H
MOV DL, 'L'
INT 21H

MOV DH, 13
MOV DL, 24
INT 10H
MOV DL, 'L'
INT 21H

MOV DH, 17
MOV DL, 31
INT 10H
MOV DL, 'O'
INT 21H

MOV AH, 08
INT 21H

MOV AH, 4CH
INT 21H
END
