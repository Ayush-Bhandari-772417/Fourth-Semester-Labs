.MODEL SMALL
.STACK 64

.DATA
COUNT EQU 10    
TBL1 DW 245H, 7895H, 4225H, 6539H, 1020H, 2134H, 3548H, 9223H, 8025H, 1EFAH
TBL2 DW 8123H, 6733H, 9242H, 5026H, 2023H, 0FFA4H, 1203H, 0F1AFH, 3048H, 0FFEEH
TBL3 DW 10 DUP(?)

.CODE
MOV AX, @DATA
MOV DS, AX
MOV CX, COUNT
MOV BX, 0000
NEXT:
	MOV AX, TBL1[BX]
	CMP AX, TBL2[BX]
	JNC RES
	MOV TBL3[BX], 1FFFH
	JMP L1
RES:	MOV TBL3[BX], 0000H
L1:	INC BX
	INC BX
	LOOP NEXT
	MOV AX, 4C00H
	INT 21H
END