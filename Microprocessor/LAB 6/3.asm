.MODEL SMALL
.STACK 64

.DATA
COUNT EQU 10
TBL1 DW 6553, 1234, 2345, 7656, 13486, 21345, 5145, 365, 786, 2077
TBL2 DW 65355, 4213, 5432, 2035, 1024, 1000, 34567, 40287, 59213, 65318
TBL3 DW 20 DUP(?)

.CODE
MOV AX, @DATA
MOV DS, AX
MOV CX, COUNT
MOV SI, 0000
MOV BX, 0000
SUM:
	MOV DX, 0000
	MOV AX, TBL1[BX]
	ADD AX, TBL2[BX]
	ADC DX, 0
	MOV TBL3[BX+SI], AX
	MOV TBL3[BX+SI+2], DX
	INC BX
	INC BX
	INC SI
	INC SI
	LOOP SUM
	MOV AX, 4C00H
	INT 21H
END