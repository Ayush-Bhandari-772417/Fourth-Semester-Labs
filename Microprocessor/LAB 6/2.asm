.MODEL SMALL
.STACK 64

.DATA
COUNT EQU 000AH
ARR DW 30105, 8085, 6054, 1111, 20134, 7865, 9012, 51375, 4004, 65530
RES DW 2 DUP(?)

.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX
MOV CX, COUNT
MOV SI, OFFSET ARR
MOV AX, 0000H
MOV BX, AX
SUM:
	ADD AX, [SI]
	ADC BX, 0
	INC SI
	INC SI
	LOOP SUM
	MOV RES, AX
	MOV RES+2, BX
	MOV AH, 4CH
	INT 21H
MAIN ENDP
	END MAIN